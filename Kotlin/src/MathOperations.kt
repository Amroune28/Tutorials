fun main(args: Array<String>) {
    var num1 = 10
    var num2 = 20
    var sum = num1 + num2 - 30
    var sub = num1 - num2
    var mul = num1 * num2
    var div = num1 / num2.toDouble()
    var mod = num1 % num2
    println("sum: $sum")
    println("sub: $sub")
    println("mul: $mul")
    println("div: $div")
    println("mod: $mod")
}