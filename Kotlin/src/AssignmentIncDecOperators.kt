fun main(args: Array<String>) {
    var n : Int = 10
    // Assignment Operators
    n += 5 //n = n + 5
    println(n)
    n -= 3 // n = n - 3
    println(n)
    n *= 2
    println(n)
    n /= 2
    println(n)
    n %= 10 // n = n % 10
    println(n)

    // Increment & Decrement Operators
    var num = 15
    num++
    println("Num: $num")
    num--
    println("Num: $num")
    println(--num)
    println(num)
}